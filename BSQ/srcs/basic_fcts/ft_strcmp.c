int	ft_strcmp(char *s1, char *s2)
{
	int i;
	int ordre;

	i = 0;
	while (s1[i] == s2[i])
	{
		if ((s1[i] == '\0') && (s2[i] == '\0'))
		{
			return (0);
		}
		i++;
	}
	ordre = s1[i] - s2[i];
	return (ordre);
}
