int	ft_nbrlen(char *str)
{
	int i;
	int count;

	i = 0;
	while (str[i] == '0')
		i++;
	count = 0;
	while ((str[i] >= '0') && (str[i] <= '9'))
	{
		count++;
		i++;
	}
	return (count);
}

