int	somme(int a, int b)
{
	return (a + b);
}

int	soustr(int a, int b)
{
	return (a - b);
}

int	div(int a, int b)
{
	return (a / b);
}

int	multi(int a, int b)
{
	return (a * b);
}

int	mod(int a, int b)
{
	return (a % b);
}
